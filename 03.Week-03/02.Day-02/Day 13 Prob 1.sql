SELECT 
    p.product_name,
    b.brand_name,
    c.category_name,
    p.model_year,
    p.list_price
FROM products AS p
INNER JOIN brands AS b 
    ON p.brand_id = b.brand_id
INNER JOIN categories AS c 
    ON p.category_id = c.category_id
WHERE p.list_price > 500
ORDER BY p.list_price ASC;

